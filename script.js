let currentLang = localStorage.getItem('lang') || 'fr';

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let value = translations[currentLang];
        for (const key of keys) {
            value = value ? value[key] : null;
        }
        if (value) {
            element.textContent = value;
        }
    });
    document.getElementById('langLabel').textContent = currentLang === 'fr' ? 'EN' : 'FR';
    document.documentElement.lang = currentLang;
}

document.getElementById('langToggle').addEventListener('click', function () {
    currentLang = currentLang === 'fr' ? 'en' : 'fr';
    localStorage.setItem('lang', currentLang);
    applyTranslations();
});

$(document).ready(function () {
    applyTranslations();

    $('.navbar-nav .nav-link').on('click', function () {
        // Don't close navbar when clicking dropdown toggle
        if ($(this).hasClass('dropdown-toggle')) return;
        $('.navbar-collapse').collapse('hide');
    });

    $(window).scroll(function () {
        if ($(this).scrollTop() > 50) {
            $('.navbar').addClass('scrolled');
        } else {
            $('.navbar').removeClass('scrolled');
        }

        if ($(this).scrollTop() > 300) {
            $('#backToTop').addClass('show');
        } else {
            $('#backToTop').removeClass('show');
        }
    });

    $('#backToTop').on('click', function () {
        $('html, body').animate({scrollTop: 0}, 600);
        return false;
    });

    $('a[href^="#"]').on('click', function (event) {
        const href = $(this).attr('href');
        // Skip if href is just "#" or empty (external links, modals, etc.)
        if (!href || href === '#' || href === '#!') return;
        
        const target = $(href);
        if (target.length) {
            event.preventDefault();
            const targetOffset = target.offset().top - 70;
            $('html, body').animate({
                scrollTop: targetOffset
            }, 600);
        }
    });

    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver(function (entries) {
        entries.forEach(function (entry) {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.card, section').forEach(function (el) {
        el.classList.add('fade-in');
        observer.observe(el);
    });

    // Contact form uses mailto action
    const contactForm = document.getElementById('contactForm');
    if (contactForm) {
        contactForm.addEventListener('submit', function (e) {
            e.preventDefault();
            const prenom = document.getElementById('prenom').value;
            const nom = document.getElementById('nom').value;
            const email = document.getElementById('email').value;
            const message = document.getElementById('message').value;

            const subject = encodeURIComponent('Nouveau message - Ionic Patrimoine');
            const body = encodeURIComponent(
                'Prénom=' + prenom + '\r\n' +
                'Nom=' + nom + '\r\n' +
                'Email=' + email + '\r\n' +
                'Message=' + message
            );

            window.location.href = 'mailto:info@ionicpatrimoine.com?subject=' + subject + '&body=' + body;
        });
    }

    const navLinks = document.querySelectorAll('.navbar-nav .nav-link');
    const sections = document.querySelectorAll('section');

    $(window).on('scroll', function () {
        let current = '';

        sections.forEach(function (section) {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;

            if ($(this).scrollTop() >= sectionTop - 150) {
                current = section.getAttribute('id');
            }
        });

        navLinks.forEach(function (link) {
            link.classList.remove('active');
            if (link.getAttribute('href') === '#' + current) {
                link.classList.add('active');
            }
        });
    });

    if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
        $('.fade-in').removeClass('fade-in');
    }
});

document.addEventListener('DOMContentLoaded', function () {
    const style = document.createElement('style');
    style.textContent = `
        .toast-notification {
            position: fixed;
            bottom: 30px;
            left: 50%;
            transform: translateX(-50%) translateY(100px);
            background: #28a745;
            color: white;
            padding: 15px 30px;
            border-radius: 50px;
            box-shadow: 0 5px 20px rgba(0,0,0,0.2);
            z-index: 9999;
            transition: transform 0.3s ease;
        }
        .toast-notification.show {
            transform: translateX(-50%) translateY(0);
        }
    `;
    document.head.appendChild(style);

    // Testimonials Carousel
    const track = document.querySelector('.testimonial-track');
    const indicators = document.querySelectorAll('.indicator');
    let currentIndex = 0;
    let autoplayInterval = null;
    const AUTOPLAY_DELAY = 5000;

    function goToSlide(index) {
        currentIndex = index;
        if (track) {
            track.style.transform = `translateX(-${index * 100}%)`;
        }
        indicators.forEach((ind, i) => {
            ind.classList.toggle('active', i === index);
        });
    }

    function nextSlide() {
        const nextIndex = (currentIndex + 1) % 4;
        goToSlide(nextIndex);
    }

    function startAutoplay() {
        stopAutoplay();
        autoplayInterval = setInterval(nextSlide, AUTOPLAY_DELAY);
    }

    function stopAutoplay() {
        if (autoplayInterval) {
            clearInterval(autoplayInterval);
            autoplayInterval = null;
        }
    }

    function resetAutoplay() {
        stopAutoplay();
        startAutoplay();
    }

    if (track && indicators.length) {
        indicators.forEach((indicator, index) => {
            indicator.addEventListener('click', () => {
                goToSlide(index);
                resetAutoplay();
            });
        });

        const prevBtn = document.querySelector('.arrow-prev');
        const nextBtn = document.querySelector('.arrow-next');

        if (prevBtn && nextBtn) {
            prevBtn.addEventListener('click', () => {
                const prevIndex = currentIndex === 0 ? 3 : currentIndex - 1;
                goToSlide(prevIndex);
                resetAutoplay();
            });

            nextBtn.addEventListener('click', () => {
                const nextIndex = (currentIndex + 1) % 4;
                goToSlide(nextIndex);
                resetAutoplay();
            });
        }

        startAutoplay();

        const carousel = document.querySelector('.testimonial-carousel');
        carousel.addEventListener('mouseenter', stopAutoplay);
        carousel.addEventListener('mouseleave', startAutoplay);
    }
});